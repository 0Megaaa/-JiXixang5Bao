package com.soft.bean;

public class ViewBar {

	
	private String year;
	
	private String autoChannel;
	private String monthServic;
	private String tollChannel;

	
	
	public ViewBar() {
		super();
	}



	 


	public String getYear() {
		return year;
	}






	public void setYear(String year) {
		this.year = year;
	}






	public String getAutoChannel() {
		return autoChannel;
	}



	public void setAutoChannel(String autoChannel) {
		this.autoChannel = autoChannel;
	}



	public String getMonthServic() {
		return monthServic;
	}



	public void setMonthServic(String monthServic) {
		this.monthServic = monthServic;
	}



	public String getTollChannel() {
		return tollChannel;
	}



	public void setTollChannel(String tollChannel) {
		this.tollChannel = tollChannel;
	}

	 
	
	
	
}
